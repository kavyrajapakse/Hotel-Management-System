    private void kButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton4ActionPerformed

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a user to delete.");
            return;
        }

        String userId = jTable1.getValueAt(selectedRow, 0).toString();
        if (userId.equals("8")) {
            JOptionPane.showMessageDialog(this, "You cannot delete the super admin.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this user?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                MySQL.executeIUD("DELETE FROM user WHERE user_id = '" + userId + "'");
                JOptionPane.showMessageDialog(this, "User deleted successfully.");
                loadUsersToTable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Delete failed: " + e.getMessage());
            }
        }
        
        clearFields();

    }//GEN-LAST:event_kButton4ActionPerformed

    private void kButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton1ActionPerformed

        String username = jTextField4.getText();
        String password = jTextField2.getText();
        String roleItem = (String) jComboBox2.getSelectedItem();

        if (username.isEmpty() || password.isEmpty() || roleItem == null) {
            JOptionPane.showMessageDialog(this, "Please fill all fields.");
            return;
        }


        

        try {
            
            ResultSet rs = MySQL.executeSearch("SELECT * FROM user WHERE username = '" + username + "'");
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Username already exists.");
                return;
            }
             String roleId = roleItem.split(" - ")[0];
            
            MySQL.executeIUD("INSERT INTO user(username, password, user_roles_id) VALUES ('" + username + "', '" + password + "', '" + roleId + "')");
            JOptionPane.showMessageDialog(this, "User registered successfully!");
            loadUsersToTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Registration failed: " + e.getMessage());
        }
        
        clearFields();

    }//GEN-LAST:event_kButton1ActionPerformed

    private void kButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton2ActionPerformed

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a user to update.");
            return;
        }
        
        

        String userId = jTable1.getValueAt(selectedRow, 0).toString();
        if (userId.equals("8")) {
            JOptionPane.showMessageDialog(this, "You cannot modify the super admin.");
            return;
        }
        String username = jTextField4.getText();
        String password = jTextField2.getText();
        String roleItem = (String) jComboBox2.getSelectedItem();
        String roleId = roleItem.split(" - ")[0];

        try {
            MySQL.executeIUD("UPDATE user SET password = '" + password + "', user_roles_id = '" + roleId + "' WHERE user_id = '" + userId + "'");
            JOptionPane.showMessageDialog(this, "User updated successfully!");
            loadUsersToTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Update failed: " + e.getMessage());
        }
        
        clearFields();

    }//GEN-LAST:event_kButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int selectedRow = jTable1.getSelectedRow();
        jTextField4.setEditable(false);

        if (selectedRow != -1) {
            jTextField4.setText(jTable1.getValueAt(selectedRow, 1).toString()); // Username
            jTextField2.setText(jTable1.getValueAt(selectedRow, 2).toString()); // Password
            String selectedRole = jTable1.getValueAt(selectedRow, 4).toString(); // Role name only

            for (int i = 0; i < jComboBox2.getItemCount(); i++) {
                if (jComboBox2.getItemAt(i).endsWith(" - " + selectedRole)) {
                    jComboBox2.setSelectedIndex(i);
                    break;
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked


