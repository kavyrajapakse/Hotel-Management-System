    private void loadUsersToTable() {
        try {
            ResultSet rs = MySQL.executeSearch("SELECT user.user_id, user.username, user.password, user.reg_date, user_roles.role_name FROM user INNER JOIN user_roles ON user.user_roles_id = user_roles.id");

            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector<String> v = new Vector<>();
                v.add(rs.getString(1)); // User ID
                v.add(rs.getString(2)); // Username
                v.add(rs.getString(3)); // Password
                v.add(rs.getString(4)); // Joined Date
                v.add(rs.getString(5)); // User role
                dtm.addRow(v);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed to load users: " + e.getMessage());
        }
    }

    private void loadComboBoxes() {
        try {

            ResultSet membershipRs = MySQL.executeSearch("SELECT id, role_name FROM user_roles");
            jComboBox2.removeAllItems();
            while (membershipRs.next()) {
                String id = membershipRs.getString("id");
                String role = membershipRs.getString("role_name");
                jComboBox2.addItem(id + " - " + role);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading combo box data: " + e.getMessage());
        }
    }

