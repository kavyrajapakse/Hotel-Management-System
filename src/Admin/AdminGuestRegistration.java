    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();

        String guestId = String.valueOf(jTable1.getValueAt(row, 0));
        String fname = String.valueOf(jTable1.getValueAt(row, 1));
        String lname = String.valueOf(jTable1.getValueAt(row, 2));
        String mobile = String.valueOf(jTable1.getValueAt(row, 3));
        String email = String.valueOf(jTable1.getValueAt(row, 4));
        String statusName = String.valueOf(jTable1.getValueAt(row, 5));
        String countryName = String.valueOf(jTable1.getValueAt(row, 6));

        jLabel25.setText(guestId);
        jTextField6.setText(fname);
        jTextField7.setText(lname);
        jTextField11.setText(mobile);
        jTextField11.setEditable(false);
        jTextField12.setText(email);

        // Set comboBox3 (resident status) by matching name
        for (int i = 0; i < jComboBox3.getItemCount(); i++) {
            String item = jComboBox3.getItemAt(i);
            if (item.endsWith(" - " + statusName)) {
                jComboBox3.setSelectedIndex(i);
                break;
            }
        }

        // Set comboBox4 (country) by matching name
        for (int i = 0; i < jComboBox4.getItemCount(); i++) {
            String item = jComboBox4.getItemAt(i);
            if (item.endsWith(" - " + countryName)) {
                jComboBox4.setSelectedIndex(i);
                break;
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

