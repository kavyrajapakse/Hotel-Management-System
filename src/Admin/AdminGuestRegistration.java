    private void kButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton3ActionPerformed
        // GuestDetails
        String fName = jTextField6.getText();
        String lName = jTextField7.getText();
        String email = jTextField12.getText();
        String mobile = jTextField11.getText();

        if (fName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your First Name", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (lName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your Last Name", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your Mobile", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Invalid Mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your email", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email address", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String statusId = extractSelectedId(jComboBox3);
        String countryId = extractSelectedId(jComboBox4);

        try {
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `guests` WHERE `mobile` = '" + mobile + "' AND `email` = '" + email + "'");
            if (resultSet.next()) {
                JOptionPane.showMessageDialog(this, "Guest already registered!", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                String guestId = returnNextGuestId();
                String regDate = new SimpleDateFormat("yyyy-MM-dd").format(new Date());

                MySQL.executeIUD("INSERT INTO `guests` (`guest_id`, `firstName`, `lastName`, `mobile`, `email`, `status`, `country`, `reg_date`) "
                        + "VALUES ('" + guestId + "', '" + fName + "', '" + lName + "', '" + mobile + "', '" + email + "', '" + statusId + "', '" + countryId + "', '" + regDate + "')");

                JOptionPane.showMessageDialog(this, "Guest registered successfully!", "Information", JOptionPane.INFORMATION_MESSAGE);
                loadGuests();
                reset();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Guest registration failed!", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_kButton3ActionPerformed

