    private void kButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton3ActionPerformed
        int selectedRow = jTable2.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a feedback entry to delete.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String fbId = jTable2.getValueAt(selectedRow, 0).toString();

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this feedback?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            fbId = fbId.replace("'", "''");

            String query = String.format("DELETE FROM feedback WHERE fb_id = '%s'", fbId);
            MySQL.executeIUD(query);

            reset();
            loadFeedbackTable();
            setNewFeedbackId();

            JOptionPane.showMessageDialog(this, "Feedback deleted successfully!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error deleting feedback: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_kButton3ActionPerformed

    private void kButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton1ActionPerformed

        String fbId = jTextField4.getText().trim();
        String guestId = jTextField7.getText().trim();
        String reserveId = jTextField5.getText().trim();
        String comments = jTextField2.getText().trim();

        if (fbId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Feedback ID cannot be empty.");
            return;
        }

        if (guestId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Guest ID cannot be empty.");
            return;
        }

        if (reserveId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Reservation ID cannot be empty.");
            return;
        }

        if (comments.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Comments cannot be empty.");
            return;
        }

        int rating;
        try {
            rating = Integer.parseInt(jTextField3.getText().trim());
            if (rating < 1 || rating > 5) {
                JOptionPane.showMessageDialog(this, "Rating must be between 1 and 5.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid rating (1-5).");
            return;
        }

        try {

            // Check if a feedback already exists for this reservation ID
            String checkQuery = "SELECT * FROM feedback WHERE reserve_id = '" + reserveId + "'";
            ResultSet rs = MySQL.executeSearch(checkQuery); // use the correct method here

            if (rs.next()) {
                // Feedback already exists
                JOptionPane.showMessageDialog(this, "Feedback has already been submitted for this reservation.");
                return;
            }

            comments = comments.replace("'", "''");
            String query = String.format(
                    "INSERT INTO feedback (fb_id, fb_text, rating, guest_id, reserve_id) VALUES ('%s', '%s', %d, '%s', '%s')",
                    fbId, comments, rating, guestId, reserveId
            );

            MySQL.executeIUD(query);

            reset();
            loadFeedbackTable();
            setNewFeedbackId();

            JOptionPane.showMessageDialog(this, "Feedback registered successfully!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error registering feedback: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_kButton1ActionPerformed

