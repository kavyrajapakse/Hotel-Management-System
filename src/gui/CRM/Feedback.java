    private void kButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton5ActionPerformed
        try {
            JasperReport jasperReport = JasperCompileManager.compileReport("src/Reports/feedback_report_valid_final.jrxml");

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            JRTableModelDataSource dataSource = new JRTableModelDataSource(model);

            HashMap<String, Object> parameters = new HashMap<>();
            parameters.put("ReportTitle", "Guest List Report");
            parameters.put("ReportDate", new java.util.Date());

            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parameters, dataSource);

            JasperViewer.viewReport(jasperPrint, false);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error generating the report: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_kButton5ActionPerformed


