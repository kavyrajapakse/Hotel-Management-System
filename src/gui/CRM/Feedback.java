    private void setNewFeedbackId() {
        try {

            ResultSet rs = MySQL.executeSearch("SELECT MAX(fb_id) AS max_id FROM feedback");
            String newfbId = "FB-000001";

            if (rs.next()) {
                String maxId = rs.getString("max_id");
                if (maxId != null) {

                    int numericPart = Integer.parseInt(maxId.split("-")[1]);
                    numericPart++;
                    newfbId = String.format("FB-%06d", numericPart);
                }
            }

            jTextField4.setText(newfbId);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error setting feedback ID: " + e.getMessage());
        }
    }

    private void loadFeedbackTable() {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);
            ResultSet rs = MySQL.executeSearch("SELECT * FROM feedback");
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("fb_id"),
                    rs.getString("guest_id"),
                    rs.getString("reserve_id"),
                    rs.getInt("rating"),
                    rs.getString("fb_text")
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading feedback: " + e.getMessage());
        }
    }

