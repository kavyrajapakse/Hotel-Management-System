        try {

            java.util.Date fromDate = dateChooserCombo3.getSelectedDate().getTime();
            java.util.Date toDate = dateChooserCombo2.getSelectedDate().getTime();

            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            String fromDateStr = sdf.format(fromDate);
            String toDateStr = sdf.format(toDate);
            
            ResultSet rs = MySQL.executeSearch(
                    "SELECT g.guest_id, g.firstName, g.lastName, g.mobile, g.email, rs.name AS status_name, c.name AS country_name, g.reg_date "
                    + "FROM guests g "
                    + "INNER JOIN resident_status rs ON g.status = rs.id "
                    + "INNER JOIN country c ON g.country = c.id "
                            + "WHERE `reg_date` BETWEEN '" + fromDateStr + "' AND '" + toDateStr + "'"
            );

//            String query = "SELECT * FROM `guests` WHERE `reg_date` BETWEEN '" + fromDateStr + "' AND '" + toDateStr + "'";
//            ResultSet rs = MySQL.executeSearch(query);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector<String> row = new Vector<>();
                row.add(rs.getString("reg_date"));
                row.add(rs.getString("guest_id"));
                row.add(rs.getString("firstName"));
                row.add(rs.getString("lastName"));
                row.add(rs.getString("email"));
                row.add(rs.getString("status_name"));
                row.add(rs.getString("mobile"));
                row.add(rs.getString("country_name"));
                model.addRow(row);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error filtering guests: " + e.getMessage(), "Filter Error", JOptionPane.ERROR_MESSAGE);
        }

