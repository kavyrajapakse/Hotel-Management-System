    private void loadGuests() {
        try {
            ResultSet resultSet = MySQL.executeSearch(
                    "SELECT g.guest_id, g.firstName, g.lastName, g.mobile, g.email, rs.name AS status_name, c.name AS country_name, g.reg_date "
                    + "FROM guests g "
                    + "INNER JOIN resident_status rs ON g.status = rs.id "
                    + "INNER JOIN country c ON g.country = c.id"
            );

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("reg_date"));
                vector.add(resultSet.getString("guest_id"));
                vector.add(resultSet.getString("firstName"));
                vector.add(resultSet.getString("lastName"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("status_name"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("country_name"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading guest data: " + e.getMessage());
        }
    }

