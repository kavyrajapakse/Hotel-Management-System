    private void loadComboBoxes() {
        try {

            ResultSet membershipRs = MySQL.executeSearch("SELECT id, level FROM membership");
            jComboBox2.removeAllItems();
            while (membershipRs.next()) {
                String id = membershipRs.getString("id");
                String type = membershipRs.getString("level");
                jComboBox2.addItem(id + " - " + type);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading combo box data: " + e.getMessage());
        }
    }

