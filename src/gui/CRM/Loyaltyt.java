    private void setNewLoyaltyId() {
        try {

            ResultSet rs = MySQL.executeSearch("SELECT MAX(loyalty_id) AS max_id FROM loyalty");
            String newLoyaltyId = "LOY-000001";

            if (rs.next()) {
                String maxId = rs.getString("max_id");
                if (maxId != null) {

                    int numericPart = Integer.parseInt(maxId.split("-")[1]);
                    numericPart++;
                    newLoyaltyId = String.format("LOY-%06d", numericPart);
                }
            }

            jTextField2.setText(newLoyaltyId);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error setting loyalty ID: " + e.getMessage());
        }
    }

    private void loadLoyaltyData(String orderBy) {
        try {
            String query = "SELECT l.loyalty_id, l.guest_id, l.points, m.level AS membership_level "
                    + "FROM loyalty l "
                    + "INNER JOIN membership m ON l.membership_id = m.id";

            if (orderBy != null) {
                switch (orderBy) {
                    case "Oldest":
                        query += " ORDER BY l.loyalty_id ASC";
                        break;
                    case "Latest":
                        query += " ORDER BY l.loyalty_id DESC";
                        break;
                    case "Most Points":
                        query += " ORDER BY l.points DESC";
                        break;
                }
            }

            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            ResultSet rs = MySQL.executeSearch(query);

            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString("guest_id"));
                vector.add(rs.getString("loyalty_id"));
                vector.add(rs.getString("points"));
                vector.add(rs.getString("membership_level"));

                model.addRow(vector);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading loyalty data: " + e.getMessage());
        }
    }

