    private void kButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton4ActionPerformed

        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a record to delete.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String loyaltyId = jTextField2.getText().trim();

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this loyalty entry?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            String query = "DELETE FROM loyalty WHERE loyalty_id = '" + loyaltyId + "'";
            MySQL.executeIUD(query);
            JOptionPane.showMessageDialog(this, "Loyalty record deleted successfully!");

            reset();
            setNewLoyaltyId();
            loadLoyaltyData(null);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error deleting record: " + e.getMessage());
        }
    }//GEN-LAST:event_kButton4ActionPerformed

    private void kButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton1ActionPerformed

        String loyaltyId = jTextField2.getText().trim();
        String guestId = jTextField4.getText().trim();
        int points;

        if (guestId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Guest ID cannot be empty.");
            return;
        }

        try {
            points = Integer.parseInt(jTextField3.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid points input. Please enter a valid number.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (points < 0) {
            JOptionPane.showMessageDialog(this, "Points cannot be negative.");
            return;
        }

        String membershipIdWithLevel = jComboBox2.getSelectedItem().toString();
        String[] parts = membershipIdWithLevel.split(" - ");
        String membershipId = parts[0];

        if (membershipId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Membership ID cannot be empty.");
            return;
        }

        try {
            ResultSet rs = MySQL.executeSearch("SELECT * FROM loyalty WHERE guest_id = '" + guestId + "'");
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "This guest already has a loyalty record.", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error checking guest ID: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            String query = String.format(
                    "INSERT INTO loyalty (loyalty_id, points, guest_id, membership_id) VALUES ('%s', %d, '%s', '%s')",
                    loyaltyId, points, guestId, membershipId);

            MySQL.executeIUD(query);
            JOptionPane.showMessageDialog(this, "Guest registered successfully!");

            reset();
            setNewLoyaltyId();
            loadLoyaltyData(null);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error registering guest: " + e.getMessage());
        }


    }//GEN-LAST:event_kButton1ActionPerformed

