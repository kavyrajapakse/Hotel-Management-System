    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        kButton1.setEnabled(false);
        kButton3.setEnabled(false);
        int row = jTable1.getSelectedRow();

        String gid = String.valueOf(jTable1.getValueAt(row, 0));
        jTextField4.setText(gid);

        String lid = String.valueOf(jTable1.getValueAt(row, 1));
        jTextField2.setText(lid);

        String points = String.valueOf(jTable1.getValueAt(row, 2));
        jTextField3.setText(points);

        String comboValue = String.valueOf(jTable1.getValueAt(row, 3)).trim();

        for (int i = 0; i < jComboBox2.getItemCount(); i++) {
            String item = jComboBox2.getItemAt(i).toString();
            String itemLevel = item.split(" - ")[1].trim(); // get the level part

            if (itemLevel.equalsIgnoreCase(comboValue)) {
                jComboBox2.setSelectedItem(item);
                break;
            }
        }


    }//GEN-LAST:event_jTable1MouseClicked

